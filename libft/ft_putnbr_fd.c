#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int	a;
	char		c;

	a = n;
	if (a < 0)
	{
		write(fd, "-", 1);
		a = -a;
	}
	if (a / 10)
		ft_putnbr_fd(a / 10, fd);
	c = a % 10 + 48;
	write(fd, &c, 1);
}
