#include "libft.h"

void	ft_putnbr(int n)
{
	long a;
	char c;

	a = n;
	if (a < 0)
	{
		write(1, "-", 1);
		a = -a;
	}
	if (a / 10)
		ft_putnbr(a / 10);
	c = a % 10 + 48;
	write(1, &c, 1);
}
